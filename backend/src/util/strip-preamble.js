// 剥离 AI 精读稿/素材开头的客套与元叙述——对读者是纯噪音，让正文第一句就是干货。
// 原则：只咬「开头」、按句剥、要求出现"解读/材料"这类元词才动手，避免误伤正文。
// 覆盖实测样本：
//   "好的，以下是根据您提供的材料进行的结构化解读：--- ①…"
//   "需要说明的是，您提供的材料是完整的，没有"无法获取原文"的情况。以下是对材料内容的进一步解读："
//   "以下是对所提供材料的结构化解读：--- ①…"
const PATTERNS = [
  /^(好的|当然|没问题|收到|明白了?)[，,。.!！：:]*\s*/,                                       // 客套开场
  /^(以下|下面|这里?)[是为][^\n。：:！!]{0,40}?(解读|梳理|分析|整理|拆解|结构化|速览|要点|总结)[^\n。：:！!]{0,20}?[：:。.]\s*/, // "以下是…解读："
  /^(以下|下面|这里?)[是为][^\n]{0,70}?的[^\n：:。]{0,10}(解读|梳理|分析|整理|拆解|结构化|速览|要点|总结)[：:。.]\s*/, // "以下是对《标题：含冒号》的结构化解读："（容忍标题内冒号）
  /^(根据|基于)(您|你)?(提供|给出?)的[^\n。：:！!]{0,40}?[，,：:。.]\s*/,                    // "根据您提供的材料，"
  /^需要说明的是[，,][^\n]{0,80}?[。.]\s*/,                                                  // "需要说明的是，……。"
  /^您(提供|给出?)的材料[^\n]{0,80}?[。.]\s*/,                                               // "您提供的材料是完整的……。"
  /^[-—*=]{2,}\s*/,                                                                          // 开头孤立分隔线
];

export function stripPreamble(text) {
  let t = (text || '').replace(/\r/g, '').trim();
  for (let pass = 0; pass < 6; pass++) {
    let changed = false;
    for (const re of PATTERNS) {
      const next = t.replace(re, '');
      if (next !== t) { t = next.trim(); changed = true; }
    }
    if (!changed) break;
  }
  return t;
}
